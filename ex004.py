receba = input('Digite qualque coisa :')
print('TIPO: \033[1;30m{}\033[m'.format(type(receba)))
print('É um número ? \033[1;31m{}\033[m'.format(receba.isnumeric()))
print('Tem APENAS letras ? \033[1;32m{}\033[m'.format(receba.isalpha()))
print('Pode ser impresso ? \033[1;33m{}\033[m'.format(receba.isprintable()))
print('É tudo minusculo ? \033[1;34m{}\033[m'.format(receba.islower()))
print('É tudo maiusculo ? \033[1;35m{}\033[m'.format(receba.isupper()))